#ifndef EDITOR_SCENE_HPP
#define EDITOR_SCENE_HPP
#include "SageCamera.hpp"
#include "SageViewport.hpp"

struct EditorScene
{
	static SageCamera camera;
	static SageViewport vp;
	/*!*****************************************************************************
	  \brief
		Loads data the scene may need
	*******************************************************************************/
	static void Load();
	/*!*****************************************************************************
	  \brief
		Initializes the scene
	*******************************************************************************/
	static void Init();
	/*!*****************************************************************************
	  \brief
		Handles input checks in the scene
	*******************************************************************************/
	static void Input();
	/*!*****************************************************************************
	  \brief
		Updates the game scene
	*******************************************************************************/
	static void Update();
	/*!*****************************************************************************
	  \brief
		Draws the game scene
	*******************************************************************************/
	static void Draw();
	/*!*****************************************************************************
	  \brief
		Frees the game scene
	*******************************************************************************/
	static void Free();
	/*!*****************************************************************************
	  \brief
		Unloads the game scene
	*******************************************************************************/
	static void Unload();
};

#endif